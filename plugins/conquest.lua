local RegionOwners = {}

-- Fill in all "non-combat" conquest areas as beastmen-controlled
-- WEST_AHT_URHGAN and beyond are neither in- or out- of control areas, so they stay nil
for i = REGIONS.TAVNAZIA + 1, REGIONS.LIMBUS do
  RegionOwners[i] = 4
end

local GetOwnerOfZone = function(zone)
  local region = ZoneToRegion[zone]
  if region == nil then
    gFunc.Error('Zone ' .. zone .. ' does not exist in conquest region table.')
    return nil
  end
  return RegionOwners[region]
end

local GetZoneId = function()
  -- gData.GetEnvironment().Area returns zone name as a string, this gets the actual zone ID
  return AshitaCore:GetMemoryManager():GetParty():GetMemberZone(0)
end

local GetNation = function()
  -- The server adds 1 to the nation before returning it, likely because 0 is considered invalid/no data
  return AshitaCore:GetMemoryManager():GetPlayer():GetNation() + 1
end

local HasStatus = function()
  return (gData.GetBuffCount('Signet') > 0)
    or (gData.GetBuffCount('Sanction') > 0)
    or (gData.GetBuffCount('Sigil') > 0)
end

local function Initialize()
  ashita.events.register('packet_in', 'conquest_cb', function(e)
    -- Conquest data packet should be sent each time player zones
    if e.id == 0x5E then
      -- All regions have data up to Tavnazia, which is followed by non-conquest zones and then ToAU+ zones
      for i = REGIONS.RONFAURE, REGIONS.TAVNAZIA do
        local owner = struct.unpack('B', e.data, 0x1D + 1 + (i * 4)) -- owner (sandy=1,bastok=2,windy=3)
        RegionOwners[i] = owner
      end
    end
    return false
  end)
end

local function Destroy()
  ashita.events.unregister('packet_in', 'conquest_cb')
end

local function IsInControl()
  return HasStatus() and GetOwnerOfZone(GetZoneId()) == GetNation()
end

local function IsOutOfControl()
  local owner = GetOwnerOfZone(GetZoneId())
  return HasStatus() and owner ~= nil and owner ~= GetNation()
end

return {
  name = 'Conquest',
  install = function(profile)
    local store = profile.store
    profile:once('load', Initialize)
    profile:once('unload', Destroy)
    store.getters.conquestUnderControl = IsInControl
    store.getters.conquestOutOfControl = IsOutOfControl
  end,
}
